public class Intrumento {
    private int clave;
    private String marca;
    private String tipo;
    private float precio;

    public Intrumento(int clave, String marca, String tipo, float precio) {
        this.clave = clave;
        this.marca = marca;
        this.tipo = tipo;
        this.precio = precio;
    }
    

    public int getClave() {
        return clave;
    }

    public void setClave(int clave) {
        this.clave = clave;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public float getPrecio() {
        return precio;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }
    
}
